               PICKUP_LOCATION VARCHAR (100) NOT NULL ,
               DROPOFF_LOCATION VARCHAR (100) NOT NULL ,
               RIDE_DATE DATETIME NOT NULL,
               FARE DECIMAL(10,2) NOT NULL 
